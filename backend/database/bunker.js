export function generateBunkerCondition(playersCount) {
    let capacity = Math.max(2, Math.floor(playersCount / 2)); 
    const timeInside = Math.floor(Math.random() * 40) + 1; 
    const catastrophe = catastrophes[Math.floor(Math.random() * catastrophes.length)];
    const perk = bunkerPerks[Math.floor(Math.random() * bunkerPerks.length)];

    return {
        catastropheTitle: catastrophe.title,
        catastropheDescription: catastrophe.description,
        perkTitle: perk.title,
        perkDescription: perk.description,
        timeInside: timeInside,
        capacity: capacity
    };
}
